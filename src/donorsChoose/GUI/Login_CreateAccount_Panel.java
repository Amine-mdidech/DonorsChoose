    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
            if(loginEmailTxt.getText().isEmpty() || loginPassTxt.getText().isEmpty()){
                JOptionPane.showMessageDialog(null, "Fill all the fields");
            }
            else{
                String email = loginEmailTxt.getText();
                String password = loginPassTxt.getText();

                Account u= theAcollection.searchAccountemailpassword(email,password);
                if(u!=null){
                    System.out.println(" login succesful ");
                    if(u.getUser() instanceof Teacher) {
                        Teacher t = (Teacher) u.getUser();
                        new TeacherMenu_Panel().setVisible(true);
                        this.setVisible(false);
                    }
                }
            }
    }//GEN-LAST:event_jButton1ActionPerformed

