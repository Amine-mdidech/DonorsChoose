    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if(EMAIL.getText().isEmpty() || PASSWORD.getText().isEmpty() ){
                JOptionPane.showMessageDialog(null, "Fill all the fields");
            }
            else{
                String email = EMAIL.getText();
                String pass= PASSWORD.getText();
                String firstname = fn.getText();
                String lastname = ln.getText();
                String birthdate = birth.getText();
                String address=adress.getText();
                String gender = gendercombo.getSelectedItem().toString();
                String type=typecombo.getSelectedItem().toString();
                
                User u1=null;
                Profile p1=null;
                Account a1 = null;
            try {
                p1 = new Profile(firstname,lastname,birthdate,gender,address);
            } catch (GenderException ex) {
               
            }
            if(type.equals("Teacher")){
                u1 = new Teacher(p1);
                try {
				a1 = new Account(email,pass,u1);
                                JOptionPane.showMessageDialog(null, "Account created succesflly, now log in");
			}catch ( SmallPasswordException spp) {
				 JOptionPane.showMessageDialog(null, "Password too small");
			}
            }
            theAcollection.addAccount(a1);
            try {
		theAcollection.save();
            }catch(IOException cnf) {
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

